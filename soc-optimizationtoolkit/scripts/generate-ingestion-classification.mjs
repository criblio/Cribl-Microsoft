// Logs-Ingestion-fit classification GENERATOR (dev-time only; the app never
// fetches at runtime). For every Microsoft Sentinel solution, classify how
// well it fits Cribl delivery through the Azure Logs Ingestion API by reading
// its Data Connector JSON(s) and running THE shared core classifier
// (packages/core/src/domain/sentinel-content/ingestion-class.ts - the same
// pure logic the live selected-solution path uses, so the shipped tags and the
// live tag never drift).
//
// The tiers: recommended (CCF Push - native Logs Ingestion API), supported
// (CCF pull / custom-table DCR - Cribl can feed the table), legacy (agent /
// Azure Functions / name-only). See reference_ccf_cribl_ingestion.
//
// Input: a local Azure-Sentinel checkout containing Solutions/ (pass as the
// first arg or via AZ_SENTINEL_DIR). A blobless sparse clone is enough:
//   git clone --filter=blob:none --sparse --depth 1 \
//     https://github.com/Azure/Azure-Sentinel.git
//   git -C Azure-Sentinel sparse-checkout set --no-cone \
//     '**/Data Connectors/**' '**/DataConnectors/**'
// (On Windows, `git config core.longpaths true` first.)
//
// Output: packages/core/src/assets/sentinel-ingestion-classification.json
// (never hand-edit; re-run this script).
//
// Usage: node --experimental-strip-types \
//          scripts/generate-ingestion-classification.mjs <az-sentinel-dir>

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  classifyConnectorIngestion,
  classifySolutionIngestion,
} from "../packages/core/src/domain/sentinel-content/ingestion-class.ts";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.join(
  HERE,
  "..",
  "packages",
  "core",
  "src",
  "assets",
  "sentinel-ingestion-classification.json",
);

const srcArg = process.argv[2] ?? process.env.AZ_SENTINEL_DIR ?? "";
if (srcArg === "") {
  console.error("Provide the Azure-Sentinel checkout dir (containing Solutions/).");
  process.exit(1);
}
const SOLUTIONS = path.join(srcArg, "Solutions");
if (!fs.existsSync(SOLUTIONS)) {
  console.error(`No Solutions/ under ${srcArg}`);
  process.exit(1);
}

// Recursively collect *.json under any "Data Connectors"/"DataConnectors" dir.
function connectorJsons(solutionDir) {
  const out = [];
  const walk = (dir, inConnectorDir) => {
    let entries;
    try {
      entries = fs.readdirSync(dir, { withFileTypes: true });
    } catch {
      return;
    }
    for (const e of entries) {
      const full = path.join(dir, e.name);
      if (e.isDirectory()) {
        walk(full, inConnectorDir || /^data ?connectors?$/i.test(e.name));
      } else if (inConnectorDir && e.name.toLowerCase().endsWith(".json")) {
        out.push(full);
      }
    }
  };
  walk(solutionDir, false);
  return out;
}

const solutions = {};
let counts = { recommended: 0, supported: 0, legacy: 0 };
let withConnectors = 0;

for (const name of fs.readdirSync(SOLUTIONS)) {
  const dir = path.join(SOLUTIONS, name);
  if (!fs.statSync(dir).isDirectory()) continue;
  const files = connectorJsons(dir);
  if (files.length === 0) continue;
  const classes = [];
  for (const file of files) {
    let json;
    try {
      json = JSON.parse(fs.readFileSync(file, "utf8"));
    } catch {
      continue; // non-JSON / template with tokens that break parse - skip
    }
    classes.push(classifyConnectorIngestion(json));
  }
  if (classes.length === 0) continue;
  const solutionClass = classifySolutionIngestion(classes);
  solutions[name] = { tier: solutionClass.tier, kind: solutionClass.kind };
  counts[solutionClass.tier]++;
  withConnectors++;
}

const payload = {
  meta: {
    source: "Azure/Azure-Sentinel Solutions/*/Data Connectors",
    solutionCount: withConnectors,
    counts,
    note:
      "Generated by scripts/generate-ingestion-classification.mjs - do not " +
      "hand-edit. Tiers: recommended=CCF Push, supported=CCF pull/custom-table " +
      "DCR, legacy=agent/Functions/name-only.",
  },
  solutions,
};

fs.writeFileSync(OUT, JSON.stringify(payload, null, 2) + "\n");
console.log(
  `Wrote ${withConnectors} solutions -> ${path.relative(process.cwd(), OUT)}\n` +
    `  recommended=${counts.recommended} supported=${counts.supported} legacy=${counts.legacy}`,
);
